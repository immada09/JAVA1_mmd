package com.kita.first.level2.practice2;

public class Practice03 {
	// 필드
	private String name;
	private int age;
	
	// 생성자
	Practice03(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	// 메소드
	public void setName(String name) {
	this.name = name;
	}
		
	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}



}
